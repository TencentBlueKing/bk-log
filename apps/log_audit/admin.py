from django.contrib import admin
from apps.log_audit.models import (
    UserOperationRecord,
)
from apps.utils.admin import AppModelAdmin


@admin.register(UserOperationRecord)
class UserOperationRecordAdmin(AppModelAdmin):
    list_display = ["created_at", "created_by", "bk_biz_id", "record_type", "record_object_id", "action", "params"]
    search_fields = ["bk_biz_id", "created_by", "record_type", "record_object_id"]

    def has_add_permission(self, request):
        return False

    def has_delete_permission(self, request, obj=None):
        return False

    def get_actions(self, request):
        actions = super().get_actions(request)
        if "delete_selected" in actions:
            del actions["delete_selected"]
        return actions

    def get_list_display_links(self, request, list_display):
        return None
